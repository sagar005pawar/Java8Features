package com.MethodParameterReflection;

//Java Method Parameter Reflection Example
public class Calculate {  
    int add(int a, int b){  
        return (a+b);  
    }  
    int mul(int a, int b){  
        return (b*a);  
    }  
}

/*
	Note - before compiling and executing below code, 
		   first compile Calculate class by following command:
	
	javac -parameters Calculate.java  
*/